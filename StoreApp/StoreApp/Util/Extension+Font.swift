import UIKit

extension UIFont {
    class func euphemiaUCAS(ofSize fontSize: CGFloat) -> UIFont? {
        return UIFont(name: "EuphemiaUCAS", size: fontSize)
    }

    class func euphemiaUCASBold(ofSize fontSize: CGFloat) -> UIFont? {
        return UIFont(name: "EuphemiaUCAS-Bold", size: fontSize)
    }

}
